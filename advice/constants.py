def see_query(**kwargs):
  '''
  Kwargs dictionary for inputing kwargs to the dict
  '''

  q = {}
  for k, v in q:
    if k is not None:
      q[k] = v 
  return q
 
def args_query(*args):
  '''
  Args dictionary for inputting args to dict
  '''
  
  q = {}
  for k, v in q.items():
    if k is not None:
      q[k] = v 
  return q